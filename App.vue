<script>
	import {
		mapMutations
	} from 'vuex'
	export default {
		methods:{
			...mapMutations(['init']),
			getStorageData(key){
				var result = "";
				try{
					result = uni.getStorageSync(key);
				}catch(e){
					
				};
				
				
				return result;
				
			},
			initState(){
				const teacherId = this.getStorageData("teacherId") || "";
				const openId = this.getStorageData("openId") || "";
				const userName = this.getStorageData("userName") || "";
				const hasLogin = this.getStorageData("hasLogin") || false;
				const pwd = this.getStorageData("pwd");
				const data = {
					teacherId:teacherId,
					openId:openId,
					userName:userName,
					hasLogin:hasLogin,
					pwd:pwd
				}
				this.init(data)
				console.log("init"+data.hasLogin);
			},
		},
		onLaunch: function() {
			console.log('App Launch');
			this.initState()
		},
		onShow: function() {
			console.log('App Show');
			
		},
		onHide: function() {
			console.log('App Hide');
		}
	}
</script>

